			<h5> About </h5>
			<p> A reference library of preference data and links assembled by <a href="http://www.nickmattei.net">Nicholas Mattei</a> and <a href="http://www.cse.unsw.edu.au/~tw/">Toby Walsh</a>. We currently house over 3,000 datasets for use by the community.  We want to provide a comprehensive resource for the multiple research communities that deal with preferences, including computational social choice, recommender systems, data mining, machine learning, and combinatorial optimization, to name just a few. </p>

			<h5> Citation Policy </h5>
			<p> We ask that you provide a reference to our repository when publishing research based on data gathered here as this will allow other researchers to find us. Additionally, some of our datasets have citation policies specific to them, please comply with these requests. Many hours have been spent to gather, organize, and publish these datasets and we want to provide credit where credit is due.	
				<ul>
				<li> Mattei, Nicholas and Walsh, Toby. PrefLib: A Library of Preference Data. 
