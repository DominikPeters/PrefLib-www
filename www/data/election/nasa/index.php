<h5>ED-00003: Mariner Path Selection</h5>
<p>The Mariner Trajectory Selection Data Set is the votes cast by the various science teams responsible for selecting the trajectory for the 1977 interplanetary satellite.  There were a total of 10 science teams voting over 32 possible paths.  All these votes are complete but indifference was allowed between some of the objects.</p>

